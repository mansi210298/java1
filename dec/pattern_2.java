package dec;

public class pattern_2 {
	 public static void main(String[] args) {
	    	int line=1;
	    	while(line<=5)
	    	{
	    		int count=1;
	    		while(count<=line)
	    		{
	    			System.out.print(line);
	    			count=count+1;
	    		} 
	    		System.out.println();
	    		line=line+1;
	    	}
	    }
	}



