package First_one;

public class Operators {
	public static void main(String args[]) {
		int a=200;
		System.out.println(a==200);
		System.out.println(a>200);
		System.out.println(a<200);
		System.out.println(a>=200);
		System.out.println(a<=200);
		System.out.println(a!=200);
		
	}

}
