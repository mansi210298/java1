package First_one;

public class Type_Casting {
	public static void main(String args[]) {
		short salary=30000;
		int annual_salary=salary*12;    //implicit typecasting
		 System.out.println(annual_salary);
		 
		 short date=11;
		 byte new_date= (byte)date;     //explicit typecasting
		 System.out.println(new_date);
		 
		 
		 
	}
		
	}


