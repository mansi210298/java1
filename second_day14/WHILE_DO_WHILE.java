public class WHILE_DO_WHILE {
	public static void main(String args[]) {
		
		int i=1;
		while(i<=7)
		{ 
		   System.out.println(i);
		   
		   i++;
		}
		 
		int j=500;
		do
		{ System.out.println(j); 
		   j++;
		}
		  while(j<=1);
		}
		
		
	}
	


