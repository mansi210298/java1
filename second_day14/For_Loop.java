package second_day14;

public class For_Loop {
	public static void main(String args[]) {
	
		for(int i=1; i<=10; i++)    //increasing
		{System.out.println(i);    //printing the value 1 to 10
	}
        for(int j=15; j>=0; j--)
        	System.out.println(j);    //decreasing
        
	}
}